package university;

public class Exam {

	private Student student;
	private Course course;
	private Integer grade;
	
	public Exam(Student student, Course course, Integer grade) {
		this.student = student;
		this.course = course;
		this.grade = grade;
	}
	
	public Student getStudent() {
		return student;
	}
	
	public Course getCourse() {
		return course;
	}
	
	public Integer getGrade() {
		return grade;
	}
	
}
